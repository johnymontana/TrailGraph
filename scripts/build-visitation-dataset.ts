/**
 * Build the bundled NPS monthly-visitation dataset (ADR — park-detail data-viz).
 *
 * Source: NPS Visitor Use Statistics Data Package "Main_Data.csv" (1979–present), a public dataset on
 * data.gov / IRMA DataStore. Columns: UnitCode,Year,Month,Statistic,Value. We keep Statistic="TRV"
 * (Total Recreation Visits) and average the most recent N full years per month → a stable "typical"
 * 12-month profile. UnitCode lowercased == our parkCode. Output is committed to
 * lib/datasources/visitation-data.ts so the app has no runtime dependency on IRMA (AD-3 "bundle, swap
 * for live later"); re-run this script to refresh.
 *
 * Usage: pnpm visitation:build   (streams the CSV; ~tens of MB)
 */
import { writeFileSync } from 'node:fs';

const CSV_URL = 'https://irma.nps.gov/DataStore/DownloadFile/756959?Reference=2317666';
const YEARS = [2023, 2024, 2025]; // recent full years; skips the COVID-distorted 2020–21
const OUT = 'lib/datasources/visitation-data.ts';
const LINE = /^"([^"]+)",(\d+),(\d+),"([^"]+)",(-?\d+)/;

interface Acc {
  sums: number[];
  counts: number[];
}

async function main() {
  const yearSet = new Set(YEARS);
  const acc = new Map<string, Acc>();

  const res = await fetch(CSV_URL);
  if (!res.ok || !res.body) throw new Error(`download failed: ${res.status}`);

  const decoder = new TextDecoder();
  let buf = '';
  let header = true;
  let rows = 0;

  const processLine = (line: string) => {
    const m = LINE.exec(line);
    if (!m) return;
    const [, unit, yearS, monthS, stat, valS] = m;
    if (stat !== 'TRV') return;
    const year = Number(yearS);
    if (!yearSet.has(year)) return;
    const month = Number(monthS);
    if (month < 1 || month > 12) return;
    rows++;
    const key = unit.toLowerCase();
    let e = acc.get(key);
    if (!e) {
      e = { sums: Array(12).fill(0), counts: Array(12).fill(0) };
      acc.set(key, e);
    }
    e.sums[month - 1] += Number(valS);
    e.counts[month - 1] += 1;
  };

  // Stream-parse so we never hold the whole multi-hundred-MB file in memory.
  for await (const chunk of res.body as unknown as AsyncIterable<Uint8Array>) {
    buf += decoder.decode(chunk, { stream: true });
    let nl: number;
    while ((nl = buf.indexOf('\n')) >= 0) {
      const line = buf.slice(0, nl);
      buf = buf.slice(nl + 1);
      if (header) {
        header = false;
        continue;
      }
      processLine(line);
    }
  }
  if (buf) processLine(buf);

  const records = [...acc.entries()]
    .map(([parkCode, e]) => ({
      parkCode,
      monthly: e.sums.map((s, i) => (e.counts[i] ? Math.round(s / e.counts[i]) : 0)),
    }))
    .filter((r) => r.monthly.some((v) => v > 0)) // drop units with no real data
    .sort((a, b) => a.parkCode.localeCompare(b.parkCode));

  const body = records.map((r) => `  { parkCode: '${r.parkCode}', monthly: [${r.monthly.join(', ')}] },`).join('\n');
  const out = `// AUTO-GENERATED by scripts/build-visitation-dataset.ts — DO NOT EDIT BY HAND.
// Real NPS monthly recreation visits (Statistic=TRV), averaged over ${YEARS.join(', ')}.
// Source: NPS Visitor Use Statistics Data Package (data.gov / IRMA DataStore). Re-run to refresh.
import type { VisitationRecord } from './visitation';

/** The full-year range the monthly profile averages over (for honest chart labeling). */
export const VISITATION_YEARS: readonly [number, number] = [${YEARS[0]}, ${YEARS[YEARS.length - 1]}];

export const NPS_VISITATION: VisitationRecord[] = [
${body}
];
`;
  writeFileSync(OUT, out);
  console.log(`[visitation] parsed ${rows.toLocaleString()} TRV rows → wrote ${records.length} parks to ${OUT}`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
